def _extract_json(text: str) -> str:
    """Extract JSON object from LLM response, handling markdown blocks, thinking, etc."""
    import re as _re

    # Try markdown code blocks first
    if "```json" in text:
        return text.split("```json")[1].split("```")[0].strip()
    if "```" in text:
        blocks = text.split("```")
        for i in range(1, len(blocks), 2):
            block = blocks[i].strip()
            # Skip language identifiers on first line
            if block and block[0] != "{":
                lines = block.split("\n", 1)
                if len(lines) > 1:
                    block = lines[1].strip()
            if block.startswith("{"):
                return block

    # Find JSON object directly via brace matching
    match = _re.search(r"\{[^{}]*\"economic_rationale\"[^{}]*\"formula\"[^{}]*\}", text, _re.DOTALL)
    if match:
        return match.group(0)

    # Last resort: find first { to last }
    start = text.find("{")
    end = text.rfind("}")
    if start != -1 and end != -1 and end > start:
        return text[start:end + 1]

    raise ValueError(f"Could not extract JSON from LLM response: {text[:200]}...")


class FactorAgent:
    """LLM-based factor hypothesis generator with memory.

    Supports two backends selected via `backend`:
      - "moonshot" (default): Kimi K2.5 via OpenAI-compatible API
      - "anthropic": Claude Sonnet 4.5 via Anthropic SDK
    """

    BACKENDS = {
        "moonshot": {
            "base_url": "https://api.moonshot.ai/v1",
            "model": "kimi-k2.5",
            "env_key": "MOONSHOT_API_KEY",
            "temperature": 1.0,
            "max_tokens": 4096,
        },
        "anthropic": {
            "base_url": "https://api.anthropic.com/v1",
            "model": "claude-sonnet-4-6",
            "env_key": "ANTHROPIC_API_KEY",
            "temperature": 0.8,
            "max_tokens": 2048,
        },
    }

    def __init__(
        self,
        backend: str = "moonshot",
        api_key: str | None = None,
        model: str | None = None,
    ):
        if backend not in self.BACKENDS:
            raise ValueError(f"Unknown backend '{backend}'. Choose from: {list(self.BACKENDS)}")

        cfg = self.BACKENDS[backend]
        self.backend = backend
        self.model = model or cfg["model"]
        self._temperature = cfg["temperature"]
        self._max_tokens = cfg["max_tokens"]

        resolved_key = api_key or os.environ.get(cfg["env_key"], "")
        if not resolved_key:
            raise ValueError(
                f"No API key found for backend '{backend}'. "
                f"Set {cfg['env_key']} or pass api_key=."
            )

        if backend == "anthropic":
            try:
                import anthropic
                self._anthropic_client = anthropic.Anthropic(api_key=resolved_key)
                self.client = None  # not using OpenAI client
            except ImportError:
                raise ImportError("pip install anthropic  — required for Claude backend")
        else:
            self.client = OpenAI(api_key=resolved_key, base_url=cfg["base_url"])
            self._anthropic_client = None

        self.memory = AgentMemory()
        self._system_prompt = SYSTEM_PROMPT.format(grammar=grammar_description())

    def _call_llm(self, user_prompt: str) -> str:
        """Dispatch to the configured LLM backend and return raw text."""
        if self.backend == "anthropic":
            response = self._anthropic_client.messages.create(
                model=self.model,
                system=self._system_prompt,
                messages=[{"role": "user", "content": user_prompt}],
                temperature=self._temperature,
                max_tokens=self._max_tokens,
            )
            # Anthropic returns list of content blocks
            parts = []
            for block in response.content:
                if block.type == "text":
                    parts.append(block.text)
            return "\n".join(parts).strip()
        else:
            # OpenAI-compatible (Moonshot, etc.)
            response = self.client.chat.completions.create(
                model=self.model,
                messages=[
                    {"role": "system", "content": self._system_prompt},
                    {"role": "user", "content": user_prompt},
                ],
                temperature=self._temperature,
                max_tokens=self._max_tokens,
            )
            return (response.choices[0].message.content or "").strip()

    def generate_hypothesis(self, round_num: int, max_retries: int = 4) -> FactorHypothesis:
        """Generate a single factor hypothesis via the LLM.

        Returns a FactorHypothesis with the parsed expression, or raises on failure.
        """
        user_prompt = (
            f"Round {round_num}: Generate a new factor hypothesis.\n\n"
            f"{self.memory.to_prompt_context()}"
        )

        last_err = None
        for attempt in range(1, max_retries + 1):
            logger.info(f"Round {round_num}: Requesting hypothesis from {self.model}"
                        + (f" (attempt {attempt})" if attempt > 1 else ""))

            raw = self._call_llm(user_prompt)
            logger.debug(f"Raw LLM response ({len(raw)} chars): {raw[:500]}")

            try:
                json_str = _extract_json(raw)
                parsed = json.loads(json_str)
                rationale = parsed["economic_rationale"]
                formula = parsed["formula"]
                expression = parse_expression(formula)

                return FactorHypothesis(
                    round=round_num,
                    economic_rationale=rationale,
                    formula=formula,
                    expression=expression,
                )
            except (ValueError, json.JSONDecodeError, KeyError) as e:
                last_err = e
                logger.warning(f"Round {round_num} attempt {attempt} parse error: {e}")
                continue

        raise last_err  # type: ignore[misc]

    def record_round(self, record: RoundRecord) -> None:
        """Record the outcome of a round into agent memory."""
        self.memory.add_round(record)

    def get_audit_log(self) -> list[dict]:
        """Return full audit log as list of dicts."""
        return [r.to_dict() for r in self.memory.rounds]
