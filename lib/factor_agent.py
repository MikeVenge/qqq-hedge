"""
LLM-powered factor hypothesis generator.

Supports multiple backends:
  - Moonshot Kimi K2.5 (default)
  - Anthropic Claude Sonnet 4.5

Implements the paper's Algorithm 1: agentic closed-loop factor discovery.
The agent generates factor hypotheses with economic rationale (Chain-of-Thought),
receives structured feedback from backtesting, and refines its search strategy
via memory-guided iteration.
"""

