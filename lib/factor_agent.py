@dataclass
class FactorHypothesis:
    """A single hypothesis generated by the agent."""

    round: int
    economic_rationale: str
    formula: str
    expression: FactorExpression | None = None

    def to_dict(self) -> dict:
        return {
            "round": self.round,
            "economic_rationale": self.economic_rationale,
            "formula": self.formula,
        }


@dataclass
class RoundRecord:
    """Structured log entry for one round of the discovery loop."""

    round: int
    hypothesis: str
    formula: str
    metrics: dict | None = None
    decision: str = ""
    reason: str = ""
    error: str | None = None

    def to_dict(self) -> dict:
        return {k: v for k, v in asdict(self).items() if v is not None}


@dataclass
class AgentMemory:
    """Accumulated evidence across rounds — the agent's state representation."""

    rounds: list[RoundRecord] = field(default_factory=list)
    promoted_formulas: list[str] = field(default_factory=list)
    retired_formulas: list[str] = field(default_factory=list)

    def add_round(self, record: RoundRecord) -> None:
        self.rounds.append(record)
        if record.decision == "promote":
            self.promoted_formulas.append(record.formula)
        elif record.decision == "retire":
            self.retired_formulas.append(record.formula)

    def to_prompt_context(self) -> str:
        """Format memory as context for the LLM prompt."""
        if not self.rounds:
            return "No prior rounds. This is the first iteration."

        lines = [f"## Prior Discovery Rounds ({len(self.rounds)} total)\n"]

        # Summarize promoted factors
        if self.promoted_formulas:
            lines.append(f"### Promoted factors ({len(self.promoted_formulas)}):")
            for f in self.promoted_formulas:
                lines.append(f"  - `{f}`")
            lines.append("")

        # Show recent rounds in detail (last 5)
        recent = self.rounds[-5:]
        lines.append(f"### Recent rounds (last {len(recent)}):")
        for r in recent:
            status = f"[{r.decision.upper()}]" if r.decision else "[ERROR]"
            lines.append(f"\nRound {r.round} {status}:")
            lines.append(f"  Rationale: {r.hypothesis[:200]}")
            lines.append(f"  Formula: `{r.formula}`")
            if r.metrics:
                m = r.metrics
                lines.append(
                    f"  Metrics: IC t-stat={m.get('ic_t_stat', 0):.2f}, "
                    f"Sharpe={m.get('ls_sharpe', 0):.2f}, "
                    f"Ret={m.get('ls_annual_return', 0):.2%}"
                )
            if r.error:
                lines.append(f"  Error: {r.error}")
            lines.append(f"  Decision: {r.reason}")

        # Guidance
        lines.append("\n### Search guidance:")
        if self.retired_formulas:
            lines.append(f"  - {len(self.retired_formulas)} formulas retired — avoid similar patterns")
        lines.append("  - Exploit: try variants of promoted factors (different windows, transforms)")
        lines.append("  - Explore: try new signal families not yet attempted")

        return "\n".join(lines)


